package com.rodrigoneves.trafegometro;

public class Link {

    private String mTitle;
    private String mLink;

    public Link(String title, String link) {
        mTitle = title;
        mLink = link;
    }

    public String getmTitle() {
        return mTitle;
    }

    public String getmLink() {
        return mLink;
    }
}
